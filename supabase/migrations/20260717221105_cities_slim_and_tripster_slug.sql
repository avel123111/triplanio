-- TRIP-236 — Tripster provider in the cities affiliate directory.
--
-- `cities` is a sparse affiliate directory keyed on `geonameid` (city geo/identity
-- comes from `geo_gazetteer` + the `city_visits.name_i18n` snapshot, NOT from here).
-- Changes:
--   1. Drop columns that only duplicate the gazetteer and are read by nobody
--      (verified: no FE, edge function, RPC, view, trigger or FK references them).
--   2. Add `tripster_slug` + `tripster_id` — the city's slug and numeric id in the
--      Tripster experiences directory (experience.tripster.ru/experience/<slug>/).
--      Same flat-column pattern as viator_dest_id / getyourguide_id.
--   3. Seed 260 Russian cities from the Tripster feed (task 1: РФ города).
--
-- PK stays on `id` (nothing references cities.id); `geonameid` keeps its unique
-- index and remains the lookup key for provider hydration.
--
-- ddl-guard: allow-destructive — TRIP-236, contract phase. cities geo columns
-- (country_code/lat/lng/time_zone/source) are dead: geo is sourced from
-- geo_gazetteer + the city_visits.name_i18n snapshot; verified no FE / edge /
-- RPC / view / trigger / FK reads them (unused since the GeoNames v2 migration).

ALTER TABLE public.cities
  DROP COLUMN IF EXISTS country_code,
  DROP COLUMN IF EXISTS lat,
  DROP COLUMN IF EXISTS lng,
  DROP COLUMN IF EXISTS time_zone,
  DROP COLUMN IF EXISTS source;

ALTER TABLE public.cities
  ADD COLUMN IF NOT EXISTS tripster_slug text,
  ADD COLUMN IF NOT EXISTS tripster_id   text;

-- Bound the new text columns (TRIP-169 caps policy). Tripster slugs are short path
-- segments (~30 chars max in the seed); the id is a short numeric string.
ALTER TABLE public.cities
  ADD CONSTRAINT cities_tripster_slug_len CHECK (tripster_slug IS NULL OR char_length(tripster_slug) <= 128),
  ADD CONSTRAINT cities_tripster_id_len   CHECK (tripster_id   IS NULL OR char_length(tripster_id)   <= 32);

CREATE INDEX IF NOT EXISTS cities_tripster_idx
  ON public.cities (tripster_slug)
  WHERE tripster_slug IS NOT NULL;

